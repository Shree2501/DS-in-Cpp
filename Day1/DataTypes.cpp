#include <iostream>
using namespace std;
int main(){
    int myNum = 5;               // Integer 
    cout<<myNum<<endl;

    double myFloatNum = 5.99;    // Floating point number
    cout<<myFloatNum<<endl;

    char myLetter = 'D';         // Character
    cout<<myLetter<<endl;
    
    string myText = "Hello";     // String 
    cout<<myText<<endl;

    bool myBoolean = true;       // Boolean (true or false)
    cout<<myBoolean<<endl;
}